    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           boolean result=intiValue();
           if(!result){
               JOptionPane.showMessageDialog(null,"Please fill all the fields");
               return;
           }
           LibrarianSectionPojo p=new LibrarianSectionPojo();
           p.setCallNo(txtCallNo.getText());
           p.setName(txtName.getText());
           p.setAuthor(txtAuthor.getText());
           p.setPublisher(txtPublisher.getText());
           p.setQuantity(Integer.parseInt(txtQuantity.getText()));
           try{
               boolean result1;
               result1 = LibrarianSectionDAO.CheckAvalibility(p);
               if(result1){
                   JOptionPane.showMessageDialog(null,"Book added Successfully");
                   return;
               }
               else{
                  JOptionPane.showMessageDialog(null,"Book added Unsuccessfully");
                   return;
               }
           }
           catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

