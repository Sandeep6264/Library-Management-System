    private void btnAddLibrarianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddLibrarianActionPerformed
                Boolean result=validateInput();
                if(!result){
                    JOptionPane.showMessageDialog(null,"Please Enter All the Fields");
                    return;   
                }
                AdminPojo ad=new AdminPojo();
                try{
                    int id=AdminDAO.genarateId();
                     ad.setId(id);
                     ad.setName(txtName.getText());
                     ad.setPassword(String.valueOf(txtPassword.getPassword()));
                     ad.setEmail(txtEmail.getText());
                     ad.setAddress(txtAddress.getText());
                     ad.setCity(txtCity.getText());
                     ad.setContact(txtContact.getText());
                     ad.setFilds(jcFiled.getSelectedItem().toString());
                     try{
                     boolean result1=AdminDAO.addLibrarian(ad);
                     if(result1==true){
                         JOptionPane.showMessageDialog(null,"User Successfully added","Success",JOptionPane.INFORMATION_MESSAGE);
                         clearInputBox();
                         return;
                     
                      }
                      JOptionPane.showMessageDialog(null,"User UnSuccessfully added","UnSuccess",JOptionPane.INFORMATION_MESSAGE);
                     
                     }
                catch(SQLException e){
                    e.printStackTrace();
                 }
                }
                     catch(SQLException e){
                    e.printStackTrace();
                    }
                
               
                
    }//GEN-LAST:event_btnAddLibrarianActionPerformed

