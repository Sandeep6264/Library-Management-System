    private void loadAllIssuedBooks() {
        try{
       ArrayList<StudentPojo>al=StudentDAO.viewAllIssuedBooks();
       if(al.isEmpty()){
           JOptionPane.showMessageDialog(null,"Books Not Issued");
           return;
       }
       Object []row=new Object[6];
       DefaultTableModel dm=(DefaultTableModel)jtViewAllissuedBooks.getModel();
       for(StudentPojo s:al){
            row[0]=s.getId();
            row[1]=s.getBookCallNo();
            row[2]=s.getStudentId();
            row[3]=s.getStudentName();
            row[4]=s.getStudentContact();
            row[5]=s.getIssuedDate();
            dm.addRow(row);
        }
        }
        catch(SQLException ex){
            ex.printStackTrace();
        }
    }
