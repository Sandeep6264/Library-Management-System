    private void btnIssueBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueBookActionPerformed
        if(!initValue()){
            JOptionPane.showMessageDialog(null,"Please fill all the fild");
            return;
        }
        Date d=new Date();
        String date=d.toLocaleString();
        StudentPojo s=new StudentPojo();
        s.setBookCallNo(txtBookCallNo.getText());
        s.setStudentId(Integer.parseInt( txtStudentId.getText()));
        s.setStudentName(txtStudentName.getText());
        s.setStudentContact(txtStudentContact.getText());
        s.setIssuedDate(date);   
        try{
          
          int id=StudentDAO.generateStudentId();
          s.setId(id);
          int result=LibrarianSectionDAO.IssuedBook(txtBookCallNo.getText());
          if(result==0)
         {
             JOptionPane.showMessageDialog(null,"Book out of Stock");
             return;
         }
          else if(result==-1){
             JOptionPane.showConfirmDialog(null,"Some Issue to Issued Book");
             return;
          }
        boolean rs=StudentDAO.addIssuedBook(s);
        if(rs){
          
            JOptionPane.showMessageDialog(null,"Book Issued Successfully");
            return;
        }
        else{
                JOptionPane.showMessageDialog(null,"Book Issued UnSuccessfully");
                return;
        }
        
        }
        catch(SQLException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnIssueBookActionPerformed

