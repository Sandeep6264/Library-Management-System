    private void loadAllBooks() {
        try{
            ArrayList<LibrarianSectionPojo>al=LibrarianSectionDAO.viewAllBook();
            if(al.isEmpty()){
                JOptionPane.showMessageDialog(null,"Here is no book avalible","Info",JOptionPane.INFORMATION_MESSAGE);
                return;
             }
            Object []obj=new Object[8];
            DefaultTableModel tm;
            tm=(DefaultTableModel)jtViewAllBooks.getModel();
            for(LibrarianSectionPojo p :al){
                obj[0]=p.getId();
                obj[1]=p.getCallNo();
                obj[2]=p.getName();
                obj[3]=p.getAuthor();
                obj[4]=p.getPublisher();
                obj[5]=p.getQuantity();
                obj[6]=p.getIssued();
                obj[7]=p.getAddedDate();
                tm.addRow(obj);
            }
            
    }
        catch(SQLException ex){
            ex.printStackTrace();
        }
            
}
