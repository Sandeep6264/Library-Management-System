    private void onLoadLabrarian() {
        try{
          ArrayList<AdminPojo> al=AdminDAO.viewLabrarian();
          if(al.isEmpty()){
              this.dispose();
              return;
          }
          DefaultTableModel tm;
            Object [] rows;
          rows = new Object[7];
          tm=(DefaultTableModel)jtLabrarian.getModel();
          for(AdminPojo p:al){
            rows[0]=p.getId();
            rows[1]=p.getName();
            rows[2]=p.getPassword();
            rows[3]=p.getEmail();
            rows[4]=p.getAddress();
            rows[5]=p.getCity();
            rows[6]=p.getContact();
            tm.addRow(rows);
        }
        }
         catch(SQLException e){
                     JOptionPane.showMessageDialog(null,"DB Error","Error ",JOptionPane.ERROR_MESSAGE);
                     e.printStackTrace();
          }
       }
