    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
          if(txtName.getText().isEmpty()||txtPassword.getPassword().length==0)
        {
            JOptionPane.showMessageDialog(null,"fill all the fild");
            return;
        }
        UserLogin p=new UserLogin();
        p.setName(txtName.getText());
        char [] str=txtPassword.getPassword();
        String str1=String.valueOf(str);
        p.setPassword(str1);
        try{
             boolean result=LoginDAO.LibrarianLogin(p);
            if(result)
            {
                    LibrarianSection ad=new LibrarianSection();
                    ad.setVisible(true);
                    this.dispose();
                    JOptionPane.showMessageDialog(null, "Login Successfully","Success",JOptionPane.INFORMATION_MESSAGE);

                }
                else{
                    txtName.setText("");
                    txtPassword.setText("");
                     JOptionPane.showMessageDialog(null,"Wrong Name/Password","Login", JOptionPane.INFORMATION_MESSAGE);
                }
        }
        catch(SQLException e){
        
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

