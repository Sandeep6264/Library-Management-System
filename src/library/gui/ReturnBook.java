    private void btnReturnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnBookActionPerformed
      if(txtBookCallNo.getText().isEmpty()||txtStudentId.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(null,"Please Fill All the field");
          return;
      }
      int id=Integer.parseInt( txtStudentId.getText());
      String bookCallNo=txtBookCallNo.getText();
      try{
          int result=StudentDAO.checkValidation(id, bookCallNo);
          if(result==-1){
              JOptionPane.showMessageDialog(null,"Student id is Wrong");
              return;
          }
          else if(result==0){
              JOptionPane.showMessageDialog(null, "Sorry,unable to return book!");
              return;
          }
          else{
              boolean rs=StudentDAO.returnBook(id);
              if(rs){
                  JOptionPane.showMessageDialog(null, "Book returned successfully!");
                   boolean r=LibrarianSectionDAO.returnBook(bookCallNo);
                   if(r)
                       System.out.println("success");
                   else
                       System.out.println("unsuccess");
                  txtBookCallNo.setText("");
                  txtStudentId.setText("");
              }
          }
          
      }
      catch(SQLException ex){
          ex.printStackTrace();
      }
    }//GEN-LAST:event_btnReturnBookActionPerformed

